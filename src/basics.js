//Arithmetic Operators:
let a = 10;
let b = 3;

console.log(a + b); 
console.log(a - b); 
console.log(a * b); 
console.log(a / b); 
console.log(a % b); 
console.log(a ** b); 

//Conditional operators

let x = 5;
let y = "5";

console.log(x == y); 
console.log(x === y); 
console.log(x != y); 
console.log(x !== y); 
console.log(x > 3);
console.log(x <= 5);

//Logical Operators:
let a1 = true;
let b1 = false;

console.log(a1 && b1); 
console.log(a1 ||b1); 
console.log(!a1); 


